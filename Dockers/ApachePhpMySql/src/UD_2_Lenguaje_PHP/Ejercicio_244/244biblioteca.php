<?php
    declare(strict_types= 1);
    function sumar(int $num1, int $num2): int
    {
        return ($num1 + $num2);
    }

    function restar(int $num1, int $num2): int
    {
        return ($num1 - $num2);
    }

    function multiplicar(int $num1, int $num2): int
    {
        return ($num1 * $num2);
    }

    function dividir(int $num1, int $num2): float
    {
        return ($num1 / $num2);
    }
?>